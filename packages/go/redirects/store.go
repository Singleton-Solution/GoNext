// InMemoryStore is a Store backed by a sync.RWMutex-guarded map. It's
// intended for tests and short-lived development environments — there
// is no persistence and no eviction.
type InMemoryStore struct {
	mu    sync.RWMutex
	rules map[uuid.UUID]Rule

	// nowFunc replaces time.Now for tests that want deterministic
	// CreatedAt values.
	nowFunc func() time.Time
}

// NewInMemoryStore returns an empty store ready for use.
func NewInMemoryStore() *InMemoryStore {
	return &InMemoryStore{rules: make(map[uuid.UUID]Rule)}
}

// SetNowFunc overrides time.Now. For tests only.
func (s *InMemoryStore) SetNowFunc(fn func() time.Time) {
	s.mu.Lock()
	s.nowFunc = fn
	s.mu.Unlock()
}

func (s *InMemoryStore) now() time.Time {
	if s.nowFunc != nil {
		return s.nowFunc()
	}
	return time.Now()
}

// Create persists a rule. Generates a new UUID v7-ish (uuid.New) ID
// regardless of any value the caller supplied — the store, not the
// caller, owns IDs.
func (s *InMemoryStore) Create(_ context.Context, rule Rule) (Rule, error) {
	if err := validateForCreate(rule); err != nil {
		return Rule{}, err
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	// Uniqueness on (source, is_regex).
	for _, r := range s.rules {
		if r.SourcePath == rule.SourcePath && r.IsRegex == rule.IsRegex {
			return Rule{}, fmt.Errorf("%w: source=%q is_regex=%t", ErrDuplicate, rule.SourcePath, rule.IsRegex)
		}
	}
	rule.ID = uuid.New()
	rule.CreatedAt = s.now()
	rule.HitCount = 0
	rule.LastHitAt = time.Time{}
	s.rules[rule.ID] = rule
	return rule, nil
}

func (s *InMemoryStore) Get(_ context.Context, id uuid.UUID) (Rule, error) {
	s.mu.RLock()
	defer s.mu.RUnlock()
	r, ok := s.rules[id]
	if !ok {
		return Rule{}, fmt.Errorf("%w: id=%s", ErrNotFound, id)
	}
	return r, nil
}

func (s *InMemoryStore) Update(_ context.Context, rule Rule) (Rule, error) {
	if err := validateForCreate(rule); err != nil {
		return Rule{}, err
	}
	if rule.ID == uuid.Nil {
		return Rule{}, fmt.Errorf("%w: zero ID", ErrInvalidRule)
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	existing, ok := s.rules[rule.ID]
	if !ok {
		return Rule{}, fmt.Errorf("%w: id=%s", ErrNotFound, rule.ID)
	}
	// Enforce uniqueness on (source, is_regex) — but the rule's own
	// row is allowed to keep the same value.
	for id, r := range s.rules {
		if id == rule.ID {
			continue
		}
		if r.SourcePath == rule.SourcePath && r.IsRegex == rule.IsRegex {
			return Rule{}, fmt.Errorf("%w: source=%q is_regex=%t", ErrDuplicate, rule.SourcePath, rule.IsRegex)
		}
	}
	// Preserve hit counters + provenance; only editable fields move.
	existing.SourcePath = rule.SourcePath
	existing.DestinationPath = rule.DestinationPath
	existing.Status = rule.Status
	existing.IsRegex = rule.IsRegex
	s.rules[rule.ID] = existing
	return existing, nil
}

func (s *InMemoryStore) Delete(_ context.Context, id uuid.UUID) error {
	s.mu.Lock()
	defer s.mu.Unlock()
	delete(s.rules, id)
	return nil
}

func (s *InMemoryStore) List(_ context.Context, before time.Time, limit int) ([]Rule, error) {
	if limit <= 0 || limit > 500 {
		limit = 50
	}
	s.mu.RLock()
	defer s.mu.RUnlock()
	all := make([]Rule, 0, len(s.rules))
	for _, r := range s.rules {
		if !before.IsZero() && !r.CreatedAt.Before(before) {
			continue
		}
		all = append(all, r)
	}
	sort.Slice(all, func(i, j int) bool {
		return all[i].CreatedAt.After(all[j].CreatedAt)
	})
	if len(all) > limit {
		all = all[:limit]
	}
	return all, nil
}

func (s *InMemoryStore) Snapshot(_ context.Context) ([]Rule, error) {
	s.mu.RLock()
	defer s.mu.RUnlock()
	out := make([]Rule, 0, len(s.rules))
	for _, r := range s.rules {
		out = append(out, r)
	}
	sort.Slice(out, func(i, j int) bool {
		return out[i].CreatedAt.Before(out[j].CreatedAt)
	})
	return out, nil
}

func (s *InMemoryStore) BulkIncrementHits(_ context.Context, deltas []HitDelta) error {
	if len(deltas) == 0 {
		return nil
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	for _, d := range deltas {
		if d.Count <= 0 {
			continue
		}
		r, ok := s.rules[d.RuleID]
		if !ok {
			// Rule was deleted between match-time and flush-time. Not
			// fatal — the delta is dropped on the floor, which is the
			// honest answer ("we can't credit hits to a row that no
			// longer exists").
			continue
		}
		r.HitCount += d.Count
		if d.LastHitAt.After(r.LastHitAt) {
			r.LastHitAt = d.LastHitAt
		}
		s.rules[d.RuleID] = r
	}
	return nil
}
