// replacementsUsage is the help text for `gonext migrate replacements`.
const replacementsUsage = `gonext migrate replacements — emit a Markdown
report of GoNext equivalents for WordPress plugins.

Usage:
  gonext migrate replacements --plugins=<slug>[,<slug>...] [--out=PATH]
  gonext migrate replacements --scan=<wp-content/plugins> [--out=PATH]

Flags:
  --plugins string  Comma-separated list of WP plugin slugs.
  --scan    string  Directory to scan for plugin subdirectories. Each
                    direct child is treated as a slug.
  --out     string  Output file. Default: replacement-report.md in CWD.

Exit codes:
  0   report generated
  1   IO error
  2   usage error

The 'covered' status reflects today's roadmap; see the published
plugin equivalents page in the docs for the live version.`

// runReplacements is the entry point for `gonext migrate replacements`.
func runReplacements(args []string, stdout, stderr io.Writer) int {
	fs := flag.NewFlagSet("migrate replacements", flag.ContinueOnError)
	fs.SetOutput(stderr)
	var (
		pluginsFlag string
		scanFlag    string
		outFlag     string
	)
	fs.StringVar(&pluginsFlag, "plugins", "", "Comma-separated WP plugin slugs")
	fs.StringVar(&scanFlag, "scan", "", "Directory containing WP plugin subdirectories")
	fs.StringVar(&outFlag, "out", "replacement-report.md", "Output file path")
	fs.Usage = func() { fmt.Fprintln(stderr, replacementsUsage) }
	if err := fs.Parse(args); err != nil {
		return ExitUsage
	}
	if pluginsFlag == "" && scanFlag == "" {
		fmt.Fprintln(stderr, replacementsUsage)
		fmt.Fprintln(stderr, "")
		fmt.Fprintln(stderr, "gonext migrate replacements: provide --plugins or --scan")
		return ExitUsage
	}

	var slugs []string
	if pluginsFlag != "" {
		slugs = parsePluginsList(pluginsFlag)
	}
	if scanFlag != "" {
		scanned, err := scanPluginsDir(scanFlag)
		if err != nil {
			fmt.Fprintf(stderr, "gonext migrate replacements: scan %q: %v\n", scanFlag, err)
			return ExitFail
		}
		slugs = mergeSlugs(slugs, scanned)
	}
	if len(slugs) == 0 {
		fmt.Fprintln(stderr, "gonext migrate replacements: no plugins to report on")
		return ExitUsage
	}

	report := buildReport(slugs)
	if err := os.WriteFile(outFlag, []byte(report), 0o644); err != nil {
		fmt.Fprintf(stderr, "gonext migrate replacements: write %q: %v\n", outFlag, err)
		return ExitFail
	}
	fmt.Fprintf(stdout, "wrote %s (%d plugins)\n", outFlag, len(slugs))
	return ExitOK
}

// parsePluginsList splits a comma-separated slug list, trims
// whitespace, and drops empty entries.
func parsePluginsList(s string) []string {
	parts := strings.Split(s, ",")
	out := make([]string, 0, len(parts))
	for _, p := range parts {
		p = strings.TrimSpace(p)
		if p != "" {
			out = append(out, p)
		}
	}
	return dedupe(out)
}

// scanPluginsDir lists the immediate subdirectories of dir. Each
// subdir name is taken as a plugin slug — the standard layout for
// wp-content/plugins/.
func scanPluginsDir(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, err
	}
	out := make([]string, 0, len(entries))
	for _, e := range entries {
		if !e.IsDir() {
			continue
		}
		name := e.Name()
		if strings.HasPrefix(name, ".") {
			continue // skip dotted (e.g. .DS_Store, .git)
		}
		out = append(out, name)
	}
	return dedupe(out), nil
}

// mergeSlugs appends b to a, deduping. Order from a is preserved;
// new slugs from b are appended in the order they appear.
func mergeSlugs(a, b []string) []string {
	seen := make(map[string]struct{}, len(a))
	out := make([]string, 0, len(a)+len(b))
	for _, s := range a {
		if _, ok := seen[s]; !ok {
			seen[s] = struct{}{}
			out = append(out, s)
		}
	}
	for _, s := range b {
		if _, ok := seen[s]; !ok {
			seen[s] = struct{}{}
			out = append(out, s)
		}
	}
	return out
}

// dedupe removes duplicates while preserving first-seen order.
func dedupe(in []string) []string {
	seen := make(map[string]struct{}, len(in))
	out := make([]string, 0, len(in))
	for _, s := range in {
		if _, ok := seen[s]; !ok {
			seen[s] = struct{}{}
			out = append(out, s)
		}
	}
	return out
}

// buildReport assembles the Markdown report for slugs in their input
// order. Each plugin slot becomes a "## <Title>" section.
func buildReport(slugs []string) string {
	var b strings.Builder
	b.WriteString("# WordPress plugin replacement guide\n\n")
	b.WriteString("Generated by `gonext migrate replacements`. ")
	b.WriteString("Each plugin below maps to a GoNext equivalent ")
	b.WriteString("with notes on coverage and migration steps.\n\n")
	b.WriteString("Status legend:\n\n")
	b.WriteString("- **Built-in** — covered by GoNext core today.\n")
	b.WriteString("- **Plugin** — covered by an official GoNext plugin.\n")
	b.WriteString("- **Planned** — on the roadmap; track the linked issue.\n")
	b.WriteString("- **No equivalent yet** — no GoNext story; consider keeping the WP install around for this surface.\n\n")
	b.WriteString("## Summary\n\n")
	b.WriteString("| Slug | Replacement | Status |\n")
	b.WriteString("| --- | --- | --- |\n")
	for _, slug := range slugs {
		e := LookupReplacement(slug)
		b.WriteString("| `")
		b.WriteString(slug)
		b.WriteString("` | ")
		b.WriteString(e.Replacement)
		b.WriteString(" | ")
		b.WriteString(string(e.Status))
		b.WriteString(" |\n")
	}
	b.WriteString("\n## Details\n\n")
	for _, slug := range slugs {
		e := LookupReplacement(slug)
		b.WriteString("### ")
		b.WriteString(e.Title)
		b.WriteString(" (`")
		b.WriteString(slug)
		b.WriteString("`)\n\n")
		b.WriteString("**Status:** ")
		b.WriteString(string(e.Status))
		b.WriteString("\n\n")
		b.WriteString("**Replacement:** ")
		b.WriteString(e.Replacement)
		b.WriteString("\n\n")
		if e.Notes != "" {
			b.WriteString(e.Notes)
			b.WriteString("\n\n")
		}
		if e.Link != "" {
			b.WriteString("Reference: ")
			b.WriteString(e.Link)
			b.WriteString("\n\n")
		}
	}
	return b.String()
}

