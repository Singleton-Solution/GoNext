// LookupReplacement returns the registry entry for a plugin slug,
// case-insensitively. Slugs not in the registry get a "best effort"
// fallback entry with status StatusUnknown.
func LookupReplacement(slug string) ReplacementEntry {
	if e, ok := registry[strings.ToLower(slug)]; ok {
		return e
	}
	return ReplacementEntry{
		Title:       titleizeSlug(slug),
		Replacement: "Not in registry — check the published plugin equivalents page.",
		Status:      StatusUnknown,
		Notes:       "This plugin slug isn't yet on the curated list. File an issue or check the docs for migration guidance.",
		Link:        "https://github.com/Singleton-Solution/GoNext/issues/new",
	}
}

// titleizeSlug turns "advanced-custom-fields" into
// "Advanced Custom Fields" for the fallback report title.
func titleizeSlug(s string) string {
	if s == "" {
		return "(unnamed)"
	}
	parts := strings.Split(s, "-")
	for i, p := range parts {
		if p == "" {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + p[1:]
	}
	return strings.Join(parts, " ")
}

// RegistrySlugs returns the curated slug list in lexicographic order
// for tests and for the `--list-known` future flag.
func RegistrySlugs() []string {
	out := make([]string, 0, len(registry))
	for s := range registry {
		out = append(out, s)
	}
	sort.Strings(out)
	return out
}

// resolvedOutPath cleans the output path so an absent directory
// component yields a sensible diagnostic instead of a panic. Exposed
// for tests; production callers route through runReplacements.
func resolvedOutPath(p string) string {
	if p == "" {
		return "replacement-report.md"
	}
	return filepath.Clean(p)
}
